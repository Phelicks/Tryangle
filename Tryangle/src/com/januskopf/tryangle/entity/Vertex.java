package com.januskopf.tryangle.entity;

public class Vertex {
	
	private float xPos;
	private float yPos;
	
	public Vertex(float xPos, float yPos){
		this.xPos = xPos;
		this.yPos = yPos;
	}

	public float getxPos() {
		return xPos;
	}

	public float getyPos() {
		return yPos;
	}
}
